import socket as so
import ipaddress
import requests

def validate_ip_address(address):
    try:
        ip = ipaddress.ip_address(address)
        return True
    except ValueError:
        return False

def checkhttpverbs(url):
    verbs = ['GET', 'POST', 'PUT', 'DELETE']
    results = {}

    for verb in verbs:
        try:
            response = requests.request(verb, url)
            results[verb] = response.status_code
        except requests.exceptions.RequestException as e:
            results[verb] = str(e)

    return results

if __name__ == "__main__":
    url = input("Inserisci l'URL da controllare: (http://URL) ---> ")
    
    # Estrai l'IP dall'URL
    ip = url.split("//")[-1].split("/")[0].split(":")[0]
    
    if validate_ip_address(ip):
        results = checkhttpverbs(url)
        for verb, status in results.items():
            print(f"{verb}: {status}")
